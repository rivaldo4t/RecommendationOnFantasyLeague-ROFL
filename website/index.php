								<h2>Introduction</h2>
							</header>
							<p>Soccer is one of the most followed games in the world, and English Premier League(EPL) is highest earning, most competitive and most watched sports league. As soccer lovers, this experience extends beyond viewing the game and participating in Fantasy Premier Leagues. The popularity of FPL can be judged from the fact that more than 4 million people in Britain itself play the league annually. Fantasy football is a game in which participants assemble an imaginary team of real life footballers and score points based on those players' actual statistical performance or their perceived contribution on the field of play. The fantasy football league ask participants to select 15 players within a price budget. The game is run over 38 gameweeks, where each gameweek comprises of 10 matches of 20 teams. The aim of the game is to score as many points as possible. The scoring is done based on some factors, like, player appearance, goals scored, assists, clean sheets, penalties, wins. The scoring is adversely affected if goals are scored against the team, yellow or red card received, penalty misses, own goals and loss.</p>
						</section>

