								<h2>Motivation</h2>
							</header>
							<p>As soccer fans and budding data scientists, we plan to tackle the problem of selecting players in an optimal and unbiased way. Most people, around the world, place bets based on emotions, expert advice or player popularity. At best, they consider past performances or statistics to find the optimal bet. We plan to build a recommender engine that ranks the players of different categories(Forward, Midfielder, Defender, Goalkeeper) and lets the user build his own team.
							Our motivation of this project is to help soccer fans make informed decisions. Since our algorithms has no biases towards players, it helps bring relatively unknown players who are performing very well into limelight. This is due to the fact that we take into consideration unique features.</p>
